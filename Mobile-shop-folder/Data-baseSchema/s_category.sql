
  CREATE TABLE "S_CATEGORY" 
   (	"CATEGORY_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"CATEGORY_NAME" VARCHAR2(20 CHAR), 
	"CATEGORY_DESC" VARCHAR2(100 CHAR), 
	"CATEGORY_REMARKS" VARCHAR2(150 CHAR), 
	 CONSTRAINT "S_CATEGORY_PK" PRIMARY KEY ("CATEGORY_ID")
  USING INDEX  ENABLE, 
	 CONSTRAINT "S_CATEGORY_UK" UNIQUE ("CATEGORY_NAME")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "S_CATEGORY_T" 
before
insert  on "S_CATEGORY"
for each row
begin
    IF :NEW."CATEGORY_ID" IS NULL THEN
        SELECT "S_CATEGORY_SEC".nextval into :NEW."CATEGORY_ID" FROM SYS.DUAL;
    END IF;
end;
/
ALTER TRIGGER "S_CATEGORY_T" ENABLE;