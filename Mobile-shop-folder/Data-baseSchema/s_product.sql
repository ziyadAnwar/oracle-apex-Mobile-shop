  CREATE OR REPLACE EDITIONABLE TRIGGER "S_PRODUCT_T" 
before
insert  on "S_PRODUCT"
for each row
begin
    IF :NEW."PRODUCT_ID" IS NULL THEN
        SELECT "S_PRODUCT_SEQ".nextval into :NEW."PRODUCT_ID" FROM SYS.DUAL;
    END IF;
end;
/
ALTER TRIGGER "S_PRODUCT_T" ENABLE;