
  CREATE TABLE "S_VENDOR" 
   (	"VENDOR_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"VENDOR_NAME" VARCHAR2(50 CHAR), 
	"VENDOR_PHONE1" VARCHAR2(20 CHAR), 
	"VENDOR_PHONE2" VARCHAR2(20 CHAR), 
	"VENDOR_MAIL" VARCHAR2(100 CHAR), 
	"VENDOR_ADDRESS" VARCHAR2(150 CHAR), 
	"VENDOR_BALANCE" NUMBER, 
	"VENDOR_REMARKS" VARCHAR2(300 CHAR), 
	"GENDER" CHAR(1), 
	 CONSTRAINT "S_VENDOR_PK" PRIMARY KEY ("VENDOR_ID")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "S_VENDOR_T" 
before
insert  on "S_VENDOR"
for each row
begin
     IF :NEW."VENDOR_ID" IS NULL THEN
        SELECT "S_VENDOR_SEQ".nextval into :NEW."VENDOR_ID" FROM SYS.DUAL;
    END IF;
end;
/
ALTER TRIGGER "S_VENDOR_T" ENABLE;