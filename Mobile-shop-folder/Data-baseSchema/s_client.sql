
  CREATE TABLE "S_CLIENT" 
   (	"CLIENT_ID" NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY MINVALUE 1 MAXVALUE 9999999999999999999999999999 INCREMENT BY 1 START WITH 1 CACHE 20 NOORDER  NOCYCLE  NOKEEP  NOSCALE  NOT NULL ENABLE, 
	"CLIENT_NAME" VARCHAR2(50 CHAR), 
	"CLIENT_PHONE1" VARCHAR2(20 CHAR), 
	"CLIENT_PHONE2" VARCHAR2(20 CHAR), 
	"CLIENT_MAIL" VARCHAR2(100 CHAR), 
	"CLIENT_ADDRESS" VARCHAR2(150 CHAR), 
	"CLIENT_BALANCE" NUMBER, 
	"CLIENT_REMARKS" VARCHAR2(300 CHAR), 
	 CONSTRAINT "S_CLIENT_PK" PRIMARY KEY ("CLIENT_ID")
  USING INDEX  ENABLE
   ) ;

  CREATE OR REPLACE EDITIONABLE TRIGGER "S_CLIENT_T" 
before
insert  on "S_CLIENT"
for each row
begin
     IF :NEW."CLIENT_ID" IS NULL THEN
        SELECT "S_CLIENT_SEQ".nextval into :NEW."CLIENT_ID" FROM SYS.DUAL;
    END IF;
end;
/
ALTER TRIGGER "S_CLIENT_T" ENABLE;